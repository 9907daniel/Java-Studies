
public class b_variable {

	public static void main(String[] args) {
		
		
		// Variable (변수) : 데이터를 임시로 저장하는 공간
		//  EX)	int year = 2021;
		// 	 	타입 변수   =  값; 
		
				
		// **변수의 선언**
		
		
		// 1)정수 (소수점 X)
		int a;
		a = 1;
		//or
		int b= 2;
		//여기서 변수는 a,b 이다

		
		
		// 2)실수 (소수점 O)
		double c = 3.3;
		System.out.println(c + 3.3);
		//6.6
		
		
		
		// 3)문자열의 변수
		String var1 = "String in Variables";
		
		String var2;
		var2 = " + another way to make a string variable";
		
		System.out.println(var1 + var2);
		
		//
	}

}
